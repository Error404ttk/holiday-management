/**
 * วิเคราะห์ประเภทวันหยุดจากชื่อ (กรณีในฐานข้อมูลไม่ได้ระบุประเภทมา)
 */
export function guessHolidayType(name: string): string {
  if (!name) return '';
  const n = name.trim();

  // กลุ่มวันหยุดราชการ / นักขัตฤกษ์หลัก
  const publicKeywords = [
    'ขึ้นปีใหม่', 'จักรี', 'สงกรานต์', 'ฉัตรมงคล', 'พืชมงคล', 
    'วิสาขบูชา', 'อาสาฬหบูชา', 'เข้าพรรษา', 'แม่แห่งชาติ', 
    'นวมินทร', 'ปิยมหาราช', 'รัฐธรรมนูญ', 'สิ้นปี',
    'เฉลิมพระชนมพรรษา', 'มาฆบูชา', 'พระบรมราชสมภพ'
  ];

  if (publicKeywords.some(key => n.includes(key))) {
    return 'วันหยุดราชการ';
  }

  // กลุ่มวันหยุดชดเชย
  if (n.includes('ชดเชย')) {
    return 'วันหยุดชดเชย';
  }

  // กลุ่มวันหยุดพิเศษ
  if (n.includes('พิเศษ') || n.includes('กรณีพิเศษ')) {
    return 'วันหยุดพิเศษ';
  }

  return '';
}
